package Switch;

public class Switch1 {

	public static void main(String[] args) {
	   //Break is optional
		int a = 10;
		switch(a)
		{
		case 10:System.out.println("Mahesh");
		       
		case 20:System.out.println("raina");
		        break;
		default:System.out.println("suresh");
		        break;
		}

	}

}
