package Switch;

public class Switchdefault {

	public static void main(String[] args) {
		//Default can be any statement
		int a = 10;
		switch(a)
		{
		 default:System.out.println("Mahesh");
		         break;
		 case 10:System.out.println("Raina");
		         break;
		 case 20:System.out.println("Rohit");
		         break;
		         
		}

	}

}
